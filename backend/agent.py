def generate_article(topic: str):

    response = agent.invoke(
        {
            "topic": topic,
            "article": "",
            "review": "",
            "search_result": "",
            "workflow": []
        }
    )

    return {
        "article": response["article"],
        "review": response["review"].content,
        "workflow": response["workflow"]
    }

def generate_article_stream(topic: str):
    # print("Streaming started")
    initial_state = {
        "topic": topic,
        "article": "",
        "review": "",
        "search_result": "",
        "workflow": []
    }

    for event in agent.stream(initial_state):
        # print(event)
        yield event